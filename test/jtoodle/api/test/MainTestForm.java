	private boolean handledInvalidKey( Exception ex ) {
		boolean val = false;

		if( ex instanceof JToodleException ) {
			JToodleException jte = (JToodleException) ex;

			if( jte.getErrorCode() == 2 ) {
				// Error Code 2 = Invalid Key
				AuthCache.logout();
				loginMenuItemActionPerformed( null );
				val = true;
			}
		}

		return( val );
	}

